    created_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    CONSTRAINT uk_permission_code UNIQUE (permission_code)
);

COMMENT ON TABLE  sys_permission IS '权限表';
COMMENT ON COLUMN sys_permission.id IS '权限ID';
COMMENT ON COLUMN sys_permission.permission_code IS '权限编码';
COMMENT ON COLUMN sys_permission.permission_name IS '权限名称';
COMMENT ON COLUMN sys_permission.resource_type IS '资源类型: MENU/BUTTON/API';
COMMENT ON COLUMN sys_permission.resource_id IS '资源标识';
COMMENT ON COLUMN sys_permission.action IS '操作类型: READ/CREATE/UPDATE/DELETE';
COMMENT ON COLUMN sys_permission.created_at IS '创建时间';

-- ============================================================
-- 5. sys_role_permission - 角色权限关联表
-- ============================================================

CREATE TABLE sys_role_permission (
    id            UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
    role_id       UUID NOT NULL,
    permission_id UUID NOT NULL,
    CONSTRAINT fk_role_perm_role FOREIGN KEY (role_id) REFERENCES sys_role (id) ON DELETE CASCADE,
    CONSTRAINT fk_role_perm_perm FOREIGN KEY (permission_id) REFERENCES sys_permission (id) ON DELETE CASCADE,
    CONSTRAINT uk_role_permission UNIQUE (role_id, permission_id)
);

COMMENT ON TABLE  sys_role_permission IS '角色权限关联表';
COMMENT ON COLUMN sys_role_permission.id IS '关联ID';
COMMENT ON COLUMN sys_role_permission.role_id IS '角色ID';
COMMENT ON COLUMN sys_role_permission.permission_id IS '权限ID';

CREATE INDEX idx_role_perm_role_id ON sys_role_permission (role_id);
CREATE INDEX idx_role_perm_perm_id ON sys_role_permission (permission_id);

-- ============================================================
-- 6. asset - 科研资产表
-- ============================================================

CREATE TABLE asset (
    id                  UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
    asset_code          VARCHAR(64)               NOT NULL,
    title               VARCHAR(256)              NOT NULL,
    asset_type          asset_type_enum           NOT NULL,
    abstract_text       TEXT,
    keywords            VARCHAR(512),
    authors             VARCHAR(512),
    department          VARCHAR(128),
    project_id          UUID,
    status              asset_status_enum         NOT NULL DEFAULT 'DRAFT',
    classification_level classification_level_enum NOT NULL DEFAULT 'INTERNAL',
    created_by          UUID                      NOT NULL,
    created_at          TIMESTAMPTZ               NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ               NOT NULL DEFAULT NOW(),
    CONSTRAINT uk_asset_code UNIQUE (asset_code),
    CONSTRAINT fk_asset_created_by FOREIGN KEY (created_by) REFERENCES sys_user (id)
);

COMMENT ON TABLE  asset IS '科研资产表';
COMMENT ON COLUMN asset.id IS '资产ID';
COMMENT ON COLUMN asset.asset_code IS '资产编号';
COMMENT ON COLUMN asset.title IS '资产标题';
COMMENT ON COLUMN asset.asset_type IS '资产类型: PAPER-论文/REPORT-报告/PATENT-专利/DATA-数据/OTHER-其他';
COMMENT ON COLUMN asset.abstract_text IS '摘要';
COMMENT ON COLUMN asset.keywords IS '关键词(逗号分隔)';
COMMENT ON COLUMN asset.authors IS '作者列表';
COMMENT ON COLUMN asset.department IS '所属部门';
COMMENT ON COLUMN asset.project_id IS '关联项目ID';
COMMENT ON COLUMN asset.status IS '状态: DRAFT-草稿/ARCHIVED-已归档/APPROVING-审批中/BORROWED-借阅中/REVOKED-已撤销';
COMMENT ON COLUMN asset.classification_level IS '密级: PUBLIC-公开/INTERNAL-内部/CONFIDENTIAL-机密/SECRET-绝密';
COMMENT ON COLUMN asset.created_by IS '创建人ID';
COMMENT ON COLUMN asset.created_at IS '创建时间';
COMMENT ON COLUMN asset.updated_at IS '更新时间';

CREATE INDEX idx_asset_code ON asset (asset_code);
CREATE INDEX idx_asset_type ON asset (asset_type);
CREATE INDEX idx_asset_status ON asset (status);
CREATE INDEX idx_asset_created_by ON asset (created_by);
CREATE INDEX idx_asset_classification ON asset (classification_level);

