-- ============================================================
-- 科研项目成果资产全生命周期管理系统 - 数据库表结构
-- 数据库: PostgreSQL
-- ============================================================

-- ============================================================
-- 枚举类型定义
-- ============================================================

CREATE TYPE asset_type_enum AS ENUM ('PAPER', 'REPORT', 'PATENT', 'DATA', 'OTHER');
-- 资产类型: 论文/报告/专利/数据/其他

CREATE TYPE asset_status_enum AS ENUM ('DRAFT', 'ARCHIVED', 'APPROVING', 'BORROWED', 'REVOKED');
-- 资产状态: 草稿/已归档/审批中/借阅中/已撤销

CREATE TYPE classification_level_enum AS ENUM ('PUBLIC', 'INTERNAL', 'CONFIDENTIAL', 'SECRET');
-- 密级: 公开/内部/机密/绝密

CREATE TYPE circulation_status_enum AS ENUM ('PENDING', 'APPROVED', 'ACTIVE', 'RETURNED', 'OVERDUE');
-- 流转状态: 待审批/已批准/借阅中/已归还/逾期

CREATE TYPE flow_type_enum AS ENUM ('ARCHIVE', 'BORROW', 'REVOKE');
-- 审批流类型: 归档/借阅/撤销

CREATE TYPE node_type_enum AS ENUM ('SINGLE', 'ALL', 'ANY');
-- 审批节点类型: 单人审批/全员审批/任意一人审批

CREATE TYPE approval_action_enum AS ENUM ('APPROVE', 'REJECT', 'TRANSFER');
-- 审批动作: 通过/驳回/转交

CREATE TYPE approval_instance_status_enum AS ENUM ('PENDING', 'APPROVED', 'REJECTED', 'CANCELLED');
-- 审批实例状态: 审批中/已通过/已驳回/已取消

-- ============================================================
-- 1. sys_user - 用户表
-- ============================================================

CREATE TABLE sys_user (
    id          UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
    username    VARCHAR(64)  NOT NULL,
    password    VARCHAR(256) NOT NULL,
    real_name   VARCHAR(64)  NOT NULL,
    email       VARCHAR(128),
    phone       VARCHAR(20),
    department  VARCHAR(128),
    status      SMALLINT     NOT NULL DEFAULT 1,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    CONSTRAINT uk_user_username UNIQUE (username)
);

COMMENT ON TABLE  sys_user IS '用户表';
COMMENT ON COLUMN sys_user.id IS '用户ID';
COMMENT ON COLUMN sys_user.username IS '用户名';
COMMENT ON COLUMN sys_user.password IS '密码(加密存储)';
COMMENT ON COLUMN sys_user.real_name IS '真实姓名';
COMMENT ON COLUMN sys_user.email IS '邮箱';
COMMENT ON COLUMN sys_user.phone IS '手机号';
COMMENT ON COLUMN sys_user.department IS '所属部门';
COMMENT ON COLUMN sys_user.status IS '状态: 1-启用, 0-禁用';
COMMENT ON COLUMN sys_user.created_at IS '创建时间';
COMMENT ON COLUMN sys_user.updated_at IS '更新时间';

-- ============================================================
-- 2. sys_role - 角色表
-- ============================================================

CREATE TABLE sys_role (
    id          UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
    role_code   VARCHAR(64)  NOT NULL,
    role_name   VARCHAR(128) NOT NULL,
    description VARCHAR(512),
    level       INT          NOT NULL DEFAULT 0,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    CONSTRAINT uk_role_code UNIQUE (role_code)
);

COMMENT ON TABLE  sys_role IS '角色表';
COMMENT ON COLUMN sys_role.id IS '角色ID';
COMMENT ON COLUMN sys_role.role_code IS '角色编码';
COMMENT ON COLUMN sys_role.role_name IS '角色名称';
COMMENT ON COLUMN sys_role.description IS '角色描述';
COMMENT ON COLUMN sys_role.level IS '角色层级(数值越小权限越高)';
COMMENT ON COLUMN sys_role.created_at IS '创建时间';

-- ============================================================
-- 3. sys_user_role - 用户角色关联表
-- ============================================================

CREATE TABLE sys_user_role (
    id      UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
    user_id UUID NOT NULL,
    role_id UUID NOT NULL,
    CONSTRAINT fk_user_role_user FOREIGN KEY (user_id) REFERENCES sys_user (id) ON DELETE CASCADE,
    CONSTRAINT fk_user_role_role FOREIGN KEY (role_id) REFERENCES sys_role (id) ON DELETE CASCADE,
    CONSTRAINT uk_user_role UNIQUE (user_id, role_id)
);

COMMENT ON TABLE  sys_user_role IS '用户角色关联表';
COMMENT ON COLUMN sys_user_role.id IS '关联ID';
COMMENT ON COLUMN sys_user_role.user_id IS '用户ID';
COMMENT ON COLUMN sys_user_role.role_id IS '角色ID';

CREATE INDEX idx_user_role_user_id ON sys_user_role (user_id);
CREATE INDEX idx_user_role_role_id ON sys_user_role (role_id);

-- ============================================================
-- 4. sys_permission - 权限表
-- ============================================================

CREATE TABLE sys_permission (
    id             UUID GENERATED BY DEFAULT AS UUID PRIMARY KEY,
