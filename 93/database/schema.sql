-- 审批结果: 通过/驳回/转审/跳过/自动通过

-- ============================================================
-- 更新 approval_node 表 - 添加新字段
-- ============================================================

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS condition_expression VARCHAR(512);
COMMENT ON COLUMN approval_node.condition_expression IS '节点跳转条件表达式';

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS is_skippable BOOLEAN NOT NULL DEFAULT FALSE;
COMMENT ON COLUMN approval_node.is_skippable IS '是否可跳过';

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS auto_approve BOOLEAN NOT NULL DEFAULT FALSE;
COMMENT ON COLUMN approval_node.auto_approve IS '是否自动审批';

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS auto_approve_condition VARCHAR(512);
COMMENT ON COLUMN approval_node.auto_approve_condition IS '自动审批条件表达式';

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS next_node_id UUID;
COMMENT ON COLUMN approval_node.next_node_id IS '下一节点ID(链式结构)';

ALTER TABLE approval_node ADD COLUMN IF NOT EXISTS next_node_ids TEXT;
COMMENT ON COLUMN approval_node.next_node_ids IS '下一节点ID列表(JSON数组, 用于条件分支)';

CREATE INDEX idx_approval_node_next_node_id ON approval_node (next_node_id);

-- ============================================================
-- 更新 approval_instance 表 - 添加新字段
-- ============================================================

ALTER TABLE approval_instance ADD COLUMN IF NOT EXISTS current_node_id UUID;
COMMENT ON COLUMN approval_instance.current_node_id IS '当前节点ID';

ALTER TABLE approval_instance ADD COLUMN IF NOT EXISTS next_node_ids TEXT;
COMMENT ON COLUMN approval_instance.next_node_ids IS '下一节点ID列表(JSON数组)';

ALTER TABLE approval_instance ADD COLUMN IF NOT EXISTS approval_path TEXT;
COMMENT ON COLUMN approval_instance.approval_path IS '已审批路径(JSON数组)';

ALTER TABLE approval_instance ADD COLUMN IF NOT EXISTS context TEXT;
COMMENT ON COLUMN approval_instance.context IS '审批上下文(JSON格式, 包含资产信息等)';

CREATE INDEX idx_approval_instance_current_node_id ON approval_instance (current_node_id);
CREATE INDEX idx_approval_instance_status ON approval_instance (status);

