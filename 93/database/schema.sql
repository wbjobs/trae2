COMMENT ON TABLE  circulation_record IS '流转记录表';
COMMENT ON COLUMN circulation_record.id IS '流转记录ID';
COMMENT ON COLUMN circulation_record.asset_id IS '关联资产ID';
COMMENT ON COLUMN circulation_record.borrower_id IS '借阅人ID';
COMMENT ON COLUMN circulation_record.borrow_purpose IS '借阅目的';
COMMENT ON COLUMN circulation_record.borrow_date IS '借阅日期';
COMMENT ON COLUMN circulation_record.expected_return_date IS '预计归还日期';
COMMENT ON COLUMN circulation_record.actual_return_date IS '实际归还日期';
COMMENT ON COLUMN circulation_record.status IS '状态: PENDING-待审批/APPROVED-已批准/ACTIVE-借阅中/RETURNED-已归还/OVERDUE-逾期';
COMMENT ON COLUMN circulation_record.approver_id IS '审批人ID';
COMMENT ON COLUMN circulation_record.approved_at IS '审批时间';
COMMENT ON COLUMN circulation_record.created_at IS '创建时间';

CREATE INDEX idx_circulation_asset_id ON circulation_record (asset_id);
CREATE INDEX idx_circulation_borrower_id ON circulation_record (borrower_id);
CREATE INDEX idx_circulation_status ON circulation_record (status);

