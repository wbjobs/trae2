COMMENT ON COLUMN approval_node.flow_id IS '关联审批流ID';
COMMENT ON COLUMN approval_node.node_order IS '节点顺序';
COMMENT ON COLUMN approval_node.node_name IS '节点名称';
COMMENT ON COLUMN approval_node.approver_role_id IS '审批角色ID(与approver_id二选一)';
COMMENT ON COLUMN approval_node.approver_id IS '指定审批人ID(与approver_role_id二选一)';
COMMENT ON COLUMN approval_node.node_type IS '节点类型: SINGLE-单人审批/ALL-全员审批/ANY-任意一人审批';
COMMENT ON COLUMN approval_node.created_at IS '创建时间';

CREATE INDEX idx_approval_node_flow_id ON approval_node (flow_id);

-- ============================================================
-- 12. approval_instance - 审批实例表
-- ============================================================

